from pydantic import BaseModel

# Input Schema
class HeartData(BaseModel):
    age: int
    sex: int
    cp: int
    trestbps: int
    chol: int
    fbs: int
    restecg: int
    thalach: int
    exang: int
    oldpeak: float
    slope: int
    ca: int
    thal: int


# Output Schema
class PredictionResponse(BaseModel):
    heart_disease: bool